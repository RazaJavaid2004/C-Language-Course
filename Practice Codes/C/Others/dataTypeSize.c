#include <stdio.h>
//datatype size using sizeof() syntax.
int main() {
    printf("Size of int datatype is %u \n", sizeof(int));
    printf("Size of short int datatype is %u \n", sizeof(short));
    printf("Size of long int datatype is %u \n", sizeof(long));
    printf("Size of float datatype is %u \n", sizeof(float));
    printf("Size of character datatype is %u \n", sizeof(char));
    printf("Size of double datatype is %u \n", sizeof(double));

    return 0;
}